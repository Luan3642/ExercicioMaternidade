package br.com.izzi.Model;

/**
 *
 * @author Aluno
 */
public class MedicoModelll {
    private int crm;
  private String nomeMedico;
  private int telefoneMedico;
  private int celularMedico;
  private String especialidadeMedico;

    public int getCrm() {
        return crm;
    }

    public void setCrm(int crm) {
        this.crm = crm;
    }

    public String getNomeMedico() {
        return nomeMedico;
    }

    public void setNomeMedico(String nomeMedico) {
        this.nomeMedico = nomeMedico;
    }

    public int getTelefoneMedico() {
        return telefoneMedico;
    }

    public void setTelefoneMedico(int telefoneMedico) {
        this.telefoneMedico = telefoneMedico;
    }

    public int getCelularMedico() {
        return celularMedico;
    }

    public void setCelularMedico(int celularMedico) {
        this.celularMedico = celularMedico;
    }

    public String getEspecialidadeMedico() {
        return especialidadeMedico;
    }

    public void setEspecialidadeMedico(String especialidadeMedico) {
        this.especialidadeMedico = especialidadeMedico;
    }

    

    
}
